package week1.day2;

public class Practice2 {
//Print numbers from 1 to 50 but skip multiples of 5.Use a loop + continue statement.
		public static void main(String[] args) 
		{
			for(int i=1;i<=50;i++)
			{
				if(i%5==0)
				{
					continue;
					
				}
			System.out.println(i);
			}
		}		

	}
	



